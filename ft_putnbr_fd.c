#include "ft_printf.h"

void	ft_putnbr_fd(int n, int fd, int	*i)
{
	long	k;

	k = n;
	if (k < 0)
	{
		write(fd, "-", 1);
		(*i)++;
		k = k * -1;
	}
	if (k < 10)
	{
		write (fd, &"0123456789"[k], 1);
		(*i)++;
	}
	else
	{
		ft_putnbr_fd(k / 10, fd, i);
		ft_putnbr_fd(k % 10, fd, i);
	}
}
